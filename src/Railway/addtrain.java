    // Method to fetch train details based on the entered train number
    private void fetchTrainDetails() {
        String trainNumber = tno.getText();
        if (trainNumber.isEmpty()) {
            return;
        }

        try {
            pst = con.prepareStatement("SELECT * FROM train_details WHERE tno = ?");
            pst.setString(1, trainNumber);
            rs = pst.executeQuery();

            if (rs.next()) {
                tname.setText(rs.getString("tname"));
            } else {
                tname.setText("");
                cl_type.setText("");
                date.setText("");
                source.setText("");
                dest.setText("");
            }

        } catch (SQLException ex) {
            Logger.getLogger(addtrain.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public static String generatePNR() {
        return UUID.randomUUID().toString().replace("-", "").substring(0, 10).toUpperCase();
    }
