    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (con == null) {
            connect(); // Attempt to connect if not already connected
        }

        if (con == null) {
            JOptionPane.showMessageDialog(this, "Not connected to database.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String t_no = tno.getText();
        String t_name = tname.getText();
        String class_ty = cl_type.getText();
        String tdate = date.getText();
        String sr = source.getText();
        String de = dest.getText();
        String pnr = generatePNR();
        
        
        try {
            pst = con.prepareStatement("insert into train_details(tno,tname,class,date,source,dest,pnr_no)values(?,?,?,?,?,?,?)");
            pst.setString(1,t_no);
            pst.setString(2,t_name);
            pst.setString(3,class_ty);
            pst.setString(4,tdate);
            pst.setString(5,sr);
            pst.setString(6,de);
            pst.setString(7, pnr);
            int k = pst.executeUpdate();
            
            if(k==1){
                JOptionPane.showMessageDialog(this,"Booking sucessfull");
            }
            else{
                JOptionPane.showMessageDialog(this,"Booking Failed");
            }
            
            tno.setText("");
            tname.setText("");
            cl_type.setText("");
            date.setText("");
            source.setText("");
            dest.setText("");
            tno.requestFocus();
      } catch (SQLException ex) {
            Logger.getLogger(addtrain.class.getName()).log(Level.SEVERE, null, ex);
        }
        
       display d = new display(pnr);
       this.hide();
       d.setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

